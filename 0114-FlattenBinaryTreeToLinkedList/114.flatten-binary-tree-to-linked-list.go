/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func flatten(root *TreeNode)  {
    if root == nil {
		return
	}

	flatten(root.Left)
	flatten(root.Right)

	rightNode := root.Right
	
	root.Right = root.Left
	root.Left = nil
	
	tempNode := root
	for tempNode.Right != nil {
		tempNode = tempNode.Right
	}
	tempNode.Right = rightNode
}
